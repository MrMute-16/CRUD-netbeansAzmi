    private void load_table1() throws SQLException {
        DefaultTableModel model=new DefaultTableModel();
        model.addColumn("no");
        model.addColumn("id_transaksi");
        model.addColumn("tgl_trx");
        model.addColumn("id_user");
        model.addColumn("total_bayar");
        model.addColumn("bayar");
        
        try{
            int no=1;
            String sql="select * from transaksi";
            
            rs=stat.executeQuery(sql);
            
            while(rs.next()){
                model.addRow(new Object[]{no++,rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)});
            }
            jTable1.setModel(model);
        }catch (Exception e){
            
        }

    }

